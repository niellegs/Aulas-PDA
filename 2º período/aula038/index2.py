print("range 5")
for cont in range(5): # Utilizando a função range de forma padrão (Valor inicial = 0)
    print(cont)

print("range 10, 20")
for cont in range(10, 21): # Utilizando a função range definindo um valor inicial.
    print(cont)

print("range 10, 21, 2")
for cont in range(10, 21, 2): # Utilizando a função range definindo um incremento.
    print(cont)
else:
    print("Fim do laço")
    


